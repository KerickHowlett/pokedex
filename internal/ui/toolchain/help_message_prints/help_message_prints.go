package help_message_prints

// HelpMessagePrints represents the strings used in the help message.
//
// Fields:
//   - CommandsSubTitle: The subtitle for the commands section.
//   - HR: The horizontal rule.
//   - Title: The title for the help message.
type HelpMessagePrints struct {
	// CommandsSubTitle is the subtitle for the commands section.
	CommandsSubTitle string
	// HR is the horizontal rule.
	HR string
	// Title is the Title for the help message.
	Title string
}
